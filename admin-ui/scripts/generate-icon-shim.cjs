#!/usr/bin/env node
/**
 * Generates the vibecms-icons.js shim with ALL lucide-react icon exports.
 * Run: node scripts/generate-icon-shim.js
 *
 * This ensures any extension can import any lucide icon without
 * requiring changes to core.
 */
const path = require("path");
const fs = require("fs");

const lucide = require(path.join(__dirname, "../node_modules/lucide-react/dist/cjs/lucide-react.js"));

// Get all PascalCase exports that are icon components (skip lowercase helpers).
// Lucide exports each icon as Name and NameIcon — we want both.
const iconNames = Object.keys(lucide).filter(
  (k) => k[0] === k[0].toUpperCase() && typeof lucide[k] === "object"
);

const lines = [
  `// Auto-generated by scripts/generate-icon-shim.js — DO NOT EDIT MANUALLY`,
  `// Provides all ${iconNames.length} lucide-react icons to extension micro-frontends.`,
  ``,
  `function getIcon(name) {`,
  `  const icons = window.__VIBECMS_SHARED__?.icons;`,
  `  if (icons && icons[name]) return icons[name];`,
  `  const React = window.__VIBECMS_SHARED__?.React;`,
  `  const Fallback = function(props) {`,
  `    if (!React) return null;`,
  `    return React.createElement("span", {`,
  `      className: props?.className || "",`,
  `      "aria-hidden": "true",`,
  `    });`,
  `  };`,
  `  Fallback.displayName = "Icon." + name;`,
  `  return Fallback;`,
  `}`,
  ``,
  `function lazyIcon(name) {`,
  `  const Component = function(props) {`,
  `    const Real = getIcon(name);`,
  `    const React = window.__VIBECMS_SHARED__?.React;`,
  `    return React ? React.createElement(Real, props) : null;`,
  `  };`,
  `  Component.displayName = name;`,
  `  return Component;`,
  `}`,
  ``,
  `// Default export: Proxy that returns lazy icons for any name`,
  `export default new Proxy({}, {`,
  `  get(_, prop) {`,
  `    return lazyIcon(prop);`,
  `  },`,
  `});`,
  ``,
];

for (const name of iconNames) {
  lines.push(`export const ${name} = lazyIcon("${name}");`);
}

lines.push("");

const outPath = path.join(__dirname, "../public/shims/vibecms-icons.js");
fs.writeFileSync(outPath, lines.join("\n"), "utf8");
console.log(`Generated ${outPath} with ${iconNames.length} icon exports.`);
