func TestHandleNotificationTest_FallbackToUserEmail(t *testing.T) {
	h := NewFakeHost()
	p := newPlugin(h)
	// Form notification with template recipient (has {{}}) so override is needed
	h.DataCreate(ctx(), formsTable, map[string]any{
		"name":     "Test",
		"slug":     "test",
		"fields":   `[]`,
		"settings": `{}`,
		"notifications": `[{
			"enabled": true,
			"to": "{{.Field.email}}",
			"subject": "Sub",
			"body": "Body"
		}]`,
	})
	// Set up user stub
	h.UserStub[42] = &coreapi.User{ID: 42, Email: "user@example.com", Name: "Test User"}

	req := &pb.PluginHTTPRequest{
		Method: "POST",
		Body:   []byte(`{}`),
		UserId: 42,
	}
	resp, err := p.handleNotificationTest(ctx(), 1, 0, req)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if resp.StatusCode != 200 {
		t.Errorf("expected 200, got %d (body: %s)", resp.StatusCode, resp.Body)
	}
	if len(h.Sent) == 0 || h.Sent[0].To[0] != "user@example.com" {
		t.Errorf("expected email sent to user@example.com, got %v", h.Sent)
	}
}

