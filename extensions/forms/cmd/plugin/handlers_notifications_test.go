func seedFormWithNotifConfig(h *FakeHost) {
	h.DataCreate(ctx(), formsTable, map[string]any{
		"name": "Notif Form",
		"slug": "notif-form",
		"fields": `[
			{"id":"email","label":"Email","type":"email"},
			{"id":"message","label":"Message","type":"textarea"}
		]`,
		"layout":   `<form></form>`,
		"settings": `{}`,
		"notifications": `[
			{
				"enabled": true,
				"to": "admin@example.com",
				"subject": "Test from {{.FormName}}",
				"body": "<p>{{.Field.message}}</p>"
			}
		]`,
	})
}

