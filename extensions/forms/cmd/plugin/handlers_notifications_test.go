func TestHandleNotificationTest_Success(t *testing.T) {
	h := NewFakeHost()
	p := newPlugin(h)
	seedFormWithNotifConfig(h)

	body, _ := json.Marshal(map[string]any{
		"override_recipient": "tester@example.com",
		"sample_data":        map[string]any{"email": "test@example.com", "message": "Hello"},
	})
	req := &pb.PluginHTTPRequest{
		Method: "POST",
		Path:   "1/notifications/0/test",
		Body:   body,
	}
	resp, err := p.handleNotificationTest(ctx(), 1, 0, req)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if resp.StatusCode != 200 {
		t.Errorf("expected 200, got %d (body: %s)", resp.StatusCode, resp.Body)
	}
	if len(h.Sent) != 1 {
		t.Fatalf("expected 1 email, got %d", len(h.Sent))
	}
	if h.Sent[0].To[0] != "tester@example.com" {
		t.Errorf("to: got %v", h.Sent[0].To)
	}
	// Subject should have [Test] prefix
	if !strings.HasPrefix(h.Sent[0].Subject, "[Test]") {
		t.Errorf("subject should have [Test] prefix, got %q", h.Sent[0].Subject)
	}
}

